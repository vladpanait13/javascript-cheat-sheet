// This file demonstrates how to use loops in JavaScript.

// A simple 'for' loop that runs 20 times
for (let i = 0; i < 20; i++) {
    // Logs the message to the console in each iteration
    console.log("That was truly outstanding. I'm really impressed!");
}

// A 'for' loop with the current loop number
for (let i = 0; i < 20; i++) {
    // Logs the iteration number and the message
    console.log(`Iteration ${i + 1}: That was truly outstanding!`);
}